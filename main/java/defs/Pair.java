package defs;

public class Pair {
	private int decision;
	private int brandIndex;
	
	public Pair(int decision, int brandIndex) {
		super();
		this.decision = decision;
		this.brandIndex = brandIndex;
	}

	public int getDecision() {
		return decision;
	}

	public int getBrandIndex() {
		return brandIndex;
	}
	
	
	
	
}
