package defs;

public class TableIds {
	int topicUserId;
	int decisionUserId;
	int itemTopicId;
	int brandTopicId;
	int itemBrandId;
	
	public TableIds(int topicUserId, int decisionUserId, int itemTopicId,
			int brandTopicId, int itemBrandId) {
		
		this.topicUserId = topicUserId;
		this.decisionUserId = decisionUserId;
		this.itemTopicId = itemTopicId;
		this.brandTopicId = brandTopicId;
		this.itemBrandId = itemBrandId;
	}
}